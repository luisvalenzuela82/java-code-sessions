public class ProgramacionOrientadaObjetos {

    public static void main(String[] args) {
        System.out.println("Hello World!");
        /*
        Programación Orientada a Objetos (POO)
        Enfoque de programación que utiliza "objetos" para modelar 
        datos y comportamientos del mundo real. 
        En Java, estos objetos son instancias de "clases", 
        que son plantillas que definen las propiedades 
        (como variables) y comportamientos (como métodos) de los objetos. 
        
        Estructura base de una Clase:
        - Atributos
        - Constructor(es)
        - Metodos 
        - Geters y Setters
        - equals, hascode, toString
        
        Buenas prácticas:
        - Nombre Clases y Objetos de Forma Clara y Descriptiva.
          Nombres de clases deben comenzar con mayúsculas, 
          mientras que los nombres de objetos (instancias) deben empezar con 
          minúsculas.
        - Mantén los campos de una clase privados y accede a ellos 
          mediante métodos públicos (getters y setters).
        - Define constructores claros y concisos. 
          Evita sobrecargar los constructores con demasiada lógica o 
          operaciones complejas.
        */
    }
}
