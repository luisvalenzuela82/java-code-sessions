/*
Una interfaz en Java es una especie de "contrato" que define un conjunto de 
métodos que las clases pueden implementar. 

Es similar a una clase, pero sus métodos son por defecto abstractos y 
públicos, lo que significa que no tienen implementación propia.

Esto permite que diferentes clases tengan comportamientos diferentes 
mientras comparten la misma interfaz, facilitando el polimorfismo 
*/
public interface Automovil {

    void conducir();
}
