public class ProgramacionOrientadaObjetos4 {

    public static void main(String[] args) {
        System.out.println("Hello World!");
        /*
        
        Abstraer, se refiere al proceso de identificar y separar las 
        características esenciales de un concepto/objeto, 
        dejando de lado los detalles específicos o menos relevantes.
        
        Involucra simplificar un concepto/objeto complejo a sus componentes 
        básicos para entenderlo mejor
        
        En Java la abstracción se puede implementar de dos formas:
        
        Clases Abstractas: 
        Una clase abstracta puede contener métodos abstractos (sin cuerpo) 
        que deben ser implementados por las subclases, así como métodos con 
        implementación. 
        Esto permite definir una plantilla para un grupo de subclases, 
        forzando la implementación de ciertos métodos esenciales, 
        mientras se proporciona una funcionalidad común que pueden 
        compartir las subclases.
        
        Interfaces:
        Una interfaz en Java es una colección de métodos abstractos y, 
        desde Java 8, también puede incluir métodos por defecto y estáticos. 
        Una interfaz define un contrato que las clases implementadoras 
        deben seguir, especificando qué se debe hacer pero no cómo hacerlo. 
        Esto permite separar completamente la definición de las operaciones 
        de su implementación.
        
        Notas finales
        Ambos, clases abstractas e interfaces, se utilizan para lograr un 
        alto nivel de abstracción, pero tienen diferentes usos y 
        características. 
        Las clases abstractas son más adecuadas cuando las subclases 
        comparten mucho código en común, mientras que las interfaces son 
        mejores cuando las clases implementadoras comparten poca o ninguna 
        implementación de código, pero deben seguir el mismo contrato de 
        interfaz.
        */
    }
}
